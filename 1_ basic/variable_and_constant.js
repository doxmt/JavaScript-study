/* 
변수 variable
- let 사용 
- 담긴 값이 바뀔 수 있는 주머니
기존 위치에 새 값을 넣는 것이 아닌 메모리상 가리키는 위치가 변경
*/
let x = 1;
console.log(x);

/*
상수 constant
-const 사용
-담긴 값이 바뀔 수 없는 주머니
-흔히 대문자로 명명
-상수를 선언과 동시에 초기화 해야함
*/
const PI = 3.14;
console.log('원주율:', PI);

/*
식별자 identifier 명명 규칙
-영문, 한글 및 유니코드(대부분의 문자 표현) 글자, 숫자 사용 가능
-특수문자는 $ 또는 _
-숫자로 시작할 수 없음 
-공백(스페이스) 사용 불가
*/